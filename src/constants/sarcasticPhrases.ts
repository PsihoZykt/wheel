/**
 * Саркастические поздравительные фразы для победителей колеса
 */
export const SARCASTIC_PHRASES = [
  'Увы, ты победил',
  'Повезло-повезло',
  'Ну вот, опять ты',
  'Неожиданно, правда?',
  'Поздравляю, наверное',
  'Какая неожиданность',
  'Ты этого достоин... или нет',
  'Удача на твоей стороне',
  'Колесо решило именно так',
  'Ну что тут скажешь',
  'Вот так вот',
  'Судьба распорядилась',
  'Кто бы мог подумать',
  'Невероятно, но факт',
  'Ничего не поделаешь',
];

/**
 * Получить случайную саркастическую фразу
 */
export const getRandomSarcasticPhrase = (): string => {
  const randomIndex = Math.floor(Math.random() * SARCASTIC_PHRASES.length);
  return SARCASTIC_PHRASES[randomIndex];
};
